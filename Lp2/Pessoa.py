class Pessoa(object):
    def __init(self):
        self._nome=None
        self._email=None
        self._celular=None
        
    def Retorna_Nome(self):
        return self._nome
    
    def Retorna_Email(self):
        return self._email
    
    def Retorna_Celular(self):
        return self._celular
    
   
    def alterar_Nome(self,Nome):
        self._nome=Nome
        return True
    def alterar_Celular(self,Celular):
        self._celular=Celular
        return True
    def alterar_Email(self,Email):
        self._email=Email
        return True





    


    
    
    
